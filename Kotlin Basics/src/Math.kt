fun main(args: Array<String>) {

    //Numbers to Work with
    var firstnum = 1024
    var secondnum = 2048


    //Addition in Kotlin
    var resultaddition:Int?

    resultaddition = firstnum + secondnum
    println("1024 + 2048 = $resultaddition")

    //Subtraction
    var resultsubtraction:Int?

    resultsubtraction = firstnum - secondnum
    println("1024 - 2048 = $resultsubtraction")

    //Multiplication
    var resultmultiply:Int?

    resultmultiply = firstnum * secondnum
    println("1024 * 2048 = $resultmultiply")

    //Division
    var resultdivide:Int?

    resultdivide = firstnum / secondnum
    println("1024 / 2048 = $resultdivide")
}